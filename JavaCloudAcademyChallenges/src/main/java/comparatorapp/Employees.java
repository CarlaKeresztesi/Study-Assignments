package comparatorapp;

public class Employees {
    String name;
    int age, salary;

    //constructor
    public Employees(String name, int age, int salary) {
        super();
        this.name = name;
        this.age = age;
        this.salary = salary;
    }

    //getter methods
    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public int getSalary() {
        return salary;
    }



}

